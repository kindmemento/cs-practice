// A Tree is a non-linear hierarchical data structure consisting of nodes connected by edges, with a top node called the root and nodes having child nodes.
// It is used in computer science for organizing data efficiently.

// Traversal of Tree:
// Tree traversal methods are used to visit and process nodes in a tree data structure.
// - In-Order: Visit left subtree, current node, then right subtree.
// - Pre-Order: Visit current node, left subtree, then right subtree.
// - Post-Order: Visit left subtree, right subtree, then current node.

// Classification of Trees:
// Classification of Trees refer to grouping trees based on certain characteristics or criteria.
// This can involve categorizing trees based on their balance factor, degree of nodes, ordering properties, etc.
// Below are some important classification of Tree.

// By Degree: Trees can be classified based on the maximum number of children each node can have.
// By Ordering: Trees can be classified based on the ordering of nodes and subtrees.
// By Balance: Trees can be classified based on how well-balanced they are.

// Common Trees
// By Degree: Binary Tree, Ternary Tree, N-ary Tree
// By Ordering: Binary Search Tree (BST)
// By Balance: Balanced Tree, Unbalanced Tree
